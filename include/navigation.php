   document.addEventListener("DOMContentLoaded", function() {
    const texts = ["Originality.", "Innovation.", "Transformation."];
    let currentIndex = 0;
    const switchingText = document.getElementById("switching-text");

    function switchText() {
        currentIndex = (currentIndex + 1) % texts.length;
        switchingText.style.animation = 'none';  // Reset the animation
        switchingText.offsetHeight;  // Trigger reflow to restart the animation
        switchingText.style.animation = null;  // Reapply the animation

        setTimeout(() => {
            switchingText.textContent = texts[currentIndex];
        }, 0);  // Slight delay to ensure the text content changes smoothly
    }

    setInterval(switchText, 1000);  // Switch text every 4 seconds
});


</script>
