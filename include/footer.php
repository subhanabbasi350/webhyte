<script>
    // Wait for the document to fully load
    document.addEventListener("DOMContentLoaded", function() {
        // Find the popup and button elements
        var popup = document.querySelector('.popup-layout');
        var openPopupButton = document.getElementById('openPopupButton');

        // Function to open the popup
        function openPopup() {
            popup.style.display = 'block';
            document.body.style.overflow = 'hidden'; // Prevent scrolling
        }

        // Function to close the popup
        function closePopup() {
            popup.style.display = 'none';
            document.body.style.overflow = ''; // Restore scrolling
        }

        // Event listener for opening the popup when button is clicked
        if (openPopupButton) {
            openPopupButton.addEventListener('click', function(e) {
                e.preventDefault();
                openPopup();
            });
        }

        // Close popup when close button is clicked
        var closeButton = document.querySelector('.popup-close');
        if (closeButton) {
            closeButton.addEventListener('click', function(e) {
                e.preventDefault();
                closePopup();
            });
        }

        // Close popup when clicking outside of it (optional)
        window.addEventListener('click', function(event) {
            if (event.target === popup) {
                closePopup();
            }
        });

        // Optionally, close popup on pressing Esc key (optional)
        document.addEventListener('keydown', function(event) {
            if (event.key === 'Escape') {
                closePopup();
            }
        });
    });
</script>
